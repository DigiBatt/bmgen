def pause(
    limits: List[ast.Limit] | None = None,
    hours: float | None = None,
    minutes: float | None = None,
    seconds: float | None = None,
    registrations: Any = None,
):
    return target.generator.add(
        ast.Step(
            cold.ConstantCurrentCharging(),
            [
                ast.Setpoint(
                    cold.ChargingCurrent, ast.NumericLiteral(0, cold.Ampere())
                ),
                time(hours, minutes, seconds).toValue(),
            ],
            limits or [],
        )
    )


@dataclass
class time:
    hours: float | None = None
    minutes: float | None = None
    seconds: float | None = None

    def toValue(self) -> ast.Setpoint:
        value = 0.0
        if self.seconds is not None:
            value = self.seconds
        if self.minutes is not None:
            value += self.minutes * 60
        if self.hours is not None:
            value += self.hours * 3600
        return ast.Setpoint(cold.StepDuration, ast.NumericLiteral(value, cold.Second()))

    def toCold(self):
        return self.toValue().toCold()

