@autocast(current=cold.Ampere, voltage=cold.Volt)
def charge(
    current: float,
    voltage: float | None = None,
    limits: List[ast.Limit] | None = None,
    registrations: Any = None,
):
    if voltage is None:
        chargeStep = target.generator.add(
            ast.Step(
                cold.ConstantCurrentCharging(),
                [ast.Setpoint(cold.ChargingCurrent, current)],
                limits or [],
            )
        )
    else:
        chargeStep = target.generator.add(
            ast.Step(
                cold.ConstantCurrentConstantVoltageCharging(),
                [
                    ast.Setpoint(cold.ChargingCurrent, current),
                    ast.Setpoint(cold.ChargingVoltage, voltage),
                ],
                limits or [],
            )
        )
    return chargeStep


